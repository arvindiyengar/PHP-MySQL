		<?php 
			/*	switch
			Useful when there are many possible actions based on the value of single variable
			*/
			
			$a = 310;
		
			switch ($a) {
				case 10: 
					echo "a equals 0";
					break;
				case 31: 
					echo "a equals 1";
					break;
				case 2: 
					echo "a equals 2";
					break;
				
			}
		?>
